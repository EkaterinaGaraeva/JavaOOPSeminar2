package ru.geekbrains.sm1;

public class Steering {
    private Boolean right;
    private Boolean left;

    public Boolean getRight() {
        return right;
    }

    public void setRight(Boolean right) {
        this.right = right;
    }

    public Boolean getLeft() {
        return left;
    }

    public void setLeft(Boolean left) {
        this.left = left;
    }
}
